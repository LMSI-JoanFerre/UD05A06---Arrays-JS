let a = [15, 29, 62, 25, 6];

//Bucle for
for (let i = 0; i < a.length; i++) {
console.log(a[i])
}

//Bucle while
let variable1 = 0;
while(variable1 < a.length){
    console.log(a[variable1]);
    variable1++;
}

//Bucle do-while
let variable2 = 0;
do{
    console.log(a[variable2]);
    variable2++;
}while(variable2 < a.length)